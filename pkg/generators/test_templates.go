// TestTemplates provides language-specific test generation templates
// These templates are PURELY FOR GENERATING NEW TEST FILES - no source code changes
type TestTemplates struct {
	// Template functions available to all templates
	funcMap template.FuncMap
}

// TestData represents data passed to test templates
type TestData struct {
	// Package information
	PackageName string
	PackagePath string

	// Function being tested
	FunctionName string
	FunctionSig  string
	FunctionCode string

	// Language
	Language types.Language

	// Test cases
	TestCases []TestCaseData

	// Mock dependencies
	Mocks []MockData

	// Imports needed
	Imports []string

	// Custom setup/teardown
	SetupCode    string
	TeardownCode string

	// File header comments
	HeaderComment string

	// Whether to include benchmarks
	IncludeBenchmarks bool

	// Whether to include examples
	IncludeExamples bool
}

// TestCaseData represents a single test case
type TestCaseData struct {
	// Test name
	Name string

	// Description of what this test verifies
	Description string

	// Input values
	Inputs map[string]string

	// Expected output
	Expected string

	// Whether this test should expect an error
	ExpectError bool

	// Expected error message (if expecting error)
	ErrorMessage string

	// Whether this is an edge case
	IsEdgeCase bool

	// Setup specific to this test
	Setup string

	// Teardown specific to this test
	Teardown string

	// Sub-tests for table-driven or parameterized tests
	SubTests []TestCaseData
}

// MockData represents a mock dependency
type MockData struct {
	// Name of the mock
	Name string

	// Type/interface being mocked
	Type string

	// Methods to mock
	Methods []MockMethod

	// Return values
	Returns map[string]string
}

// MockMethod represents a method to mock
type MockMethod struct {
	Name       string
	Parameters []string
	Returns    []string
}

// NewTestTemplates creates a new test templates instance
func NewTestTemplates() *TestTemplates {
	return &TestTemplates{
		funcMap: template.FuncMap{
			"lower":         strings.ToLower,
			"upper":         strings.ToUpper,
			"title":         strings.Title,
			"trim":          strings.TrimSpace,
			"join":          strings.Join,
			"split":         strings.Split,
			"indent":        indent,
			"now":           getCurrentTime,
			"hasPrefix":     strings.HasPrefix,
			"hasSuffix":     strings.HasSuffix,
			"replace":       strings.ReplaceAll,
			"quote":         quoteString,
			"formatInputs":  formatInputs,
			"formatReturns": formatReturns,
			"inc":           func(i int) int { return i + 1 },
		},
	}
}

// GetTestFileTemplate returns the appropriate template for a test file
func (t *TestTemplates) GetTestFileTemplate(lang types.Language) string {
	switch lang {
	case types.Go:
		return t.getGoTestFileTemplate()
	case types.NodeJS:
		return t.getNodeJSTestFileTemplate()
	case types.Python:
		return t.getPythonTestFileTemplate()
	default:
		return t.getDefaultTestFileTemplate()
	}
}

