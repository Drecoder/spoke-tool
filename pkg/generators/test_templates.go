// GetTestFileName returns the standard test file name for a source file
func (t *TestTemplates) GetTestFileName(sourceFile string, lang types.Language) string {
	switch lang {
	case types.Go:
		// math.go -> math_test.go
		if strings.HasSuffix(sourceFile, ".go") {
			return strings.TrimSuffix(sourceFile, ".go") + "_test.go"
		}
	case types.NodeJS:
		// math.js -> math.test.js
		if strings.HasSuffix(sourceFile, ".js") {
			return strings.TrimSuffix(sourceFile, ".js") + ".test.js"
		}
		if strings.HasSuffix(sourceFile, ".ts") {
			return strings.TrimSuffix(sourceFile, ".ts") + ".test.ts"
		}
	case types.Python:
		// math.py -> test_math.py
		if strings.HasSuffix(sourceFile, ".py") {
			return "test_" + sourceFile
		}
	}
	return "test_" + sourceFile
}

// GetPackageName extracts package name from file path
func (t *TestTemplates) GetPackageName(filePath string, lang types.Language) string {
	switch lang {
	case types.Go:
		// For Go, package name is often the directory name
		parts := strings.Split(filePath, "/")
		if len(parts) > 0 {
			return parts[len(parts)-1]
		}
	case types.NodeJS:
		// For Node.js, we might not need package name in tests
		return ""
	case types.Python:
		// For Python, we might not need package name in tests
		return ""
	}
	return "main"
}
