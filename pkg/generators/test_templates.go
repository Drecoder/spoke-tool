// Template execution methods

// ExecuteTestFile executes the test file template with the given data
func (t *TestTemplates) ExecuteTestFile(data *TestData) (string, error) {
	tmpl := t.GetTestFileTemplate(data.Language)

	// Register test case template
	testCaseTmpl := t.GetTestCaseTemplate(data.Language)

	fullTmpl := testCaseTmpl + "\n" + tmpl

	return t.ExecuteTemplate(fullTmpl, data)
}

// ExecuteTemplate executes a template with the given data
func (t *TestTemplates) ExecuteTemplate(tmplStr string, data interface{}) (string, error) {
	tmpl, err := template.New("test").Funcs(t.funcMap).Parse(tmplStr)
	if err != nil {
		return "", err
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return "", err
	}

	return buf.String(), nil
}

// Helper functions for templates

func quoteString(s string) string {
	return fmt.Sprintf("%q", s)
}

func formatInputs(inputs map[string]string) string {
	var parts []string
	for k, v := range inputs {
		parts = append(parts, fmt.Sprintf("%s: %s", k, v))
	}
	return strings.Join(parts, ", ")
}

func formatReturns(returns []string) string {
	if len(returns) == 0 {
		return ""
	}
	if len(returns) == 1 {
		return returns[0]
	}
	return "(" + strings.Join(returns, ", ") + ")"
}

// Template function to convert to snake_case
func toSnakeCase(s string) string {
	var result []rune
	for i, r := range s {
		if i > 0 && r >= 'A' && r <= 'Z' {
			result = append(result, '_')
		}
		result = append(result, r)
	}
	return strings.ToLower(string(result))
}

// Template function to get type from value
func typeFromValue(value string) string {
	// Simple type inference for template
	if strings.Contains(value, "\"") {
		return "string"
	}
	if strings.Contains(value, ".") {
		return "float64"
	}
	if value == "true" || value == "false" {
		return "bool"
	}
	return "int"
}

// Template function to check if expected value uses toEqual vs toBe
func isEqual(expected string) bool {
	// Simple heuristic: use toEqual for objects/arrays
	return strings.Contains(expected, "{") || strings.Contains(expected, "[")
}

