// Go templates

func (t *TestTemplates) getGoTestFileTemplate() string {
	return `// Code generated by spoke-tool. DO NOT EDIT.
{{if .HeaderComment}}{{.HeaderComment}}
{{end}}package {{.PackageName}}

import (
	"testing"
	{{range .Imports}}"{{.}}"
	{{end}}
)

{{if .SetupCode}}
func setup() {
{{.SetupCode | indent 1}}
}

func teardown() {
{{.TeardownCode | indent 1}}
}
{{end}}

{{range .TestCases}}
{{template "go_test_case" .}}
{{end}}

{{if .IncludeBenchmarks}}
{{template "go_benchmark" .}}
{{end}}
`
}

func (t *TestTemplates) getGoTestCaseTemplate() string {
	return `{{define "go_test_case"}}func Test{{.Name}}(t *testing.T) {
	{{if .Setup}}// Setup
	{{.Setup}}
	{{end}}
	tests := []struct {
		name    string
		{{range $key, $value := .Inputs}}{{$key}} {{$value | typeFromValue}}
		{{end}}want    {{.Expected | typeFromValue}}
		wantErr bool
	}{
		{{range $i, $tc := .SubTests}}{
			name: "{{$tc.Name}}",
			{{range $key, $value := $tc.Inputs}}{{$key}}: {{$value}},
			{{end}}want: {{$tc.Expected}},
			wantErr: {{$tc.ExpectError}},
		},
		{{end}}
	}
	
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			{{if .ExpectError}}got, err := {{$.FunctionName}}({{range $key, $value := .Inputs}}tt.{{$key}}, {{end}})
			if (err != nil) != tt.wantErr {
				t.Errorf("{{$.FunctionName}}() error = %v, wantErr %v", err, tt.wantErr)
				return
			}
			if !tt.wantErr && got != tt.want {
				t.Errorf("{{$.FunctionName}}() = %v, want %v", got, tt.want)
			}{{else}}got := {{$.FunctionName}}({{range $key, $value := .Inputs}}tt.{{$key}}, {{end}})
			if got != tt.want {
				t.Errorf("{{$.FunctionName}}() = %v, want %v", got, tt.want)
			}{{end}}
		})
	}
	{{if .Teardown}}
	// Teardown
	{{.Teardown}}
	{{end}}
}
{{end}}`
}

